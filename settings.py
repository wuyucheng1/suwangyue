# ========== 消息实体类：统一前后端通信消息格式 ==========
class Message:
    # 消息事件类型（前端区分不同操作）
    event = ""
    # 消息携带的数据体
    data = {}

    def __init__(self, event, data):
        # 构造方法：初始化事件名和数据
        self.event = event
        self.data = data

# ========== 玩家实体类：存储单个玩家所有信息 ==========
class Player:
    # 玩家状态：1正常游戏 0已认输/出局
    status: bool = 1
    # 玩家昵称
    name = ''
    # 玩家拥有的“道”（游戏筹码）
    coin = 5
    # 玩家手牌：[明牌下标, 暗牌下标]
    cards = []
    # 玩家专属消息队列，用于SSE向前端推送消息
    msg_queue = queue.Queue()

    def __init__(self, name, coin, cards):
        # 构造方法：初始化昵称、筹码、手牌
        self.name = name
        self.coin = coin
        self.cards = cards

